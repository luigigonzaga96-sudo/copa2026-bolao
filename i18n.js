let currentLang = localStorage.getItem("app_lang") || (
  navigator.language && navigator.language.startsWith("es") ? "es" :
  navigator.language && navigator.language.startsWith("en") ? "en" :
  "pt-BR"
);
if (!translations[currentLang]) {
  currentLang = "pt-BR";
}

export function setLanguage(lang) {
  if (translations[lang]) {
    currentLang = lang;
    localStorage.setItem("app_lang", lang);
    applyTranslations();
    if (window.GT) {
      const activeTab = document.querySelector(".tab.tab--active");
      if (activeTab) {
        const tabName = activeTab.id.replace("tab-", "");
        window.GT(tabName);
      } else {
        window.GT("ranking");
      }
    }
  }
}

export function applyTranslations() {
  const dict = translations[currentLang] || translations["pt-BR"];
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.innerHTML = dict[key];
    }
  });
  
  const switcher = document.getElementById("lang-switcher");
  if (switcher) {
    switcher.value = currentLang;

    const expand = () => {
      const optPt = switcher.querySelector('option[value="pt-BR"]');
      const optEs = switcher.querySelector('option[value="es"]');
      const optEn = switcher.querySelector('option[value="en"]');
      if (optPt) optPt.textContent = "🇧🇷 PT";
      if (optEs) optEs.textContent = "🇪🇸 ES";
      if (optEn) optEn.textContent = "🇺🇸 EN";
    };

    const collapse = () => {
      const optPt = switcher.querySelector('option[value="pt-BR"]');
      const optEs = switcher.querySelector('option[value="es"]');
      const optEn = switcher.querySelector('option[value="en"]');
      if (optPt) optPt.textContent = "🇧🇷";
      if (optEs) optEs.textContent = "🇪🇸";
      if (optEn) optEn.textContent = "🇺🇸";
    };

    // Revert initially to flags only
    collapse();

    if (!switcher.dataset.bound) {
      switcher.dataset.bound = "true";
      switcher.addEventListener("focus", expand);
      switcher.addEventListener("mousedown", expand);
      switcher.addEventListener("change", () => {
        collapse();
        switcher.blur();
      });
      switcher.addEventListener("blur", collapse);
      switcher.addEventListener("keydown", (e) => {
        if (e.key === "Escape" || e.key === "Enter") {
          switcher.blur();
        }
      });
    }
  }
}

export function getTranslation(key) {
  const dict = translations[currentLang] || translations["pt-BR"];
  return dict[key] || key;
}

window.setLanguage = setLanguage;
